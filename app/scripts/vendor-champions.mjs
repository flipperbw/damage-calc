// One-shot vendoring script: pulls the Champions mod's authoritative
// learnsets table from smogon/pokemon-showdown and emits it as
// `app/src/data/generated/champions-learnsets.generated.ts`.
//
//   npm run vendor:champions                # use the pinned default SHA
//   npm run vendor:champions -- --ref master # latest from master
//   npm run vendor:champions -- --ref <sha> # specific commit
//
// We pull from Showdown directly rather than @pkmn/ps (its TS port): the
// port lags Showdown by weeks (it sat on a pre-M-B import while Showdown
// shipped M-B learnsets on 2026-06-17), and the Champions mod isn't in the
// published @pkmn/mods npm build regardless. We pin to a known-good commit
// by default so reruns are deterministic.
//
// The source file is a plain TS object literal — alphabetised by species
// id (lowercased, hyphens stripped), each entry shaped as
//   <id>: { learnset: { <moveid>: ["9M", "9L1", ...] } }
// Mega formes share the base species's learnset (no `charizardmegay`
// entry — query `charizard`). Forme-driven species (Aegislash, Mimikyu,
// Palafin) likewise resolve to the base species's entry.

import { writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const OUT_PATH = path.resolve(__dirname, '..', 'src', 'data', 'generated', 'champions-learnsets.generated.ts');

// smogon/pokemon-showdown @ "Champions: Fix missing signature moves" (2026-06-17),
// the latest M-B learnset commit at time of vendoring.
const DEFAULT_REF = '9e03ca096';

function arg(name) {
  const i = process.argv.indexOf(name);
  if (i < 0 || i + 1 >= process.argv.length) return undefined;
  return process.argv[i + 1];
}

async function main() {
  const ref = arg('--ref') ?? DEFAULT_REF;
  const url = `https://raw.githubusercontent.com/smogon/pokemon-showdown/${ref}/data/mods/champions/learnsets.ts`;
  console.log(`fetching ${url}`);
  const res = await fetch(url);
  if (!res.ok) throw new Error(`fetch failed: ${res.status} ${res.statusText}`);
  const src = await res.text();

  // Strip any leading imports and rename the export. The body is plain TS
  // object-literal data — no transformation needed beyond the wrapper. The
  // type annotation varies by source (Showdown inlines `import('...').Modded
  // LearnsetDataTable`; @pkmn/ps used a bare `ModdedLearnsetDataTable`), so
  // match everything up to the `= {`.
  let body = src
    .replace(/^import[^\n]*\n/gm, '')
    .replace(
      /export const Learnsets:[^=]*=\s*\{/,
      'export const CHAMPIONS_LEARNSETS: ChampionsLearnsetsTable = {',
    );

  // Sanity check the rename actually applied — otherwise the schema changed
  // upstream and we'd be emitting a syntactically broken file.
  if (!body.includes('export const CHAMPIONS_LEARNSETS')) {
    throw new Error('Upstream schema appears to have changed — could not rewrite Learnsets export.');
  }

  // Count species so we have an obvious "did the scrape work" signal.
  const speciesCount = (body.match(/^\t\w+: \{$/gm) ?? []).length;

  const header = [
    '// AUTO-GENERATED by app/scripts/vendor-champions.mjs.',
    '// Do not hand-edit — re-run the script to refresh.',
    `// Source: ${url}`,
    `// Fetched: ${new Date().toISOString()}`,
    `// Species: ${speciesCount}`,
    '',
    '/**',
    ' * Each value is a list of learnset method tags from Showdown — strings',
    ' * like "9M" (gen 9 TM), "9L1" (gen 9 level 1), "9T" (gen 9 tutor),',
    ' * "9E" (gen 9 egg), etc. Callers that just need "can this species learn',
    ' * this move?" can treat the table as `Record<id, Record<moveid, unknown>>`.',
    ' */',
    '// Some cosmetic-forme entries are bare `{}` upstream — signalling "no',
    "// extra learnset, inherits everything from the base species\". Hence",
    '// `learnset` is optional; callers should treat missing as empty.',
    'export type ChampionsLearnsetsTable = Record<string, { learnset?: Record<string, string[]> }>;',
    '',
  ].join('\n');

  await writeFile(OUT_PATH, header + body, 'utf8');
  console.log(`wrote ${OUT_PATH} (${speciesCount} species)`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
